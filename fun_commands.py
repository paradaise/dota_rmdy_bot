def get_funny_response(response_type: str, target_name: str = None) -> str:
    """Получение случайной забавной реакции"""
    if response_type not in FUNNY_RESPONSES:
        return "🤔 Что-то пошло не так..."
    
    response = random.choice(FUNNY_RESPONSES[response_type])
    
    if target_name:
        response = f"@{target_name} {response}"
    
    return response

